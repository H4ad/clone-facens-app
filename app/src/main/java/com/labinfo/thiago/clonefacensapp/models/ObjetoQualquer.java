package com.labinfo.thiago.clonefacensapp.models;

public class ObjetoQualquer {

    String title;
    String message;

    public ObjetoQualquer(String title, String message) {
        this.title = title;
        this.message = message;
    }

    public String getTitle() {
        return title;
    }

    public String getMessage() {
        return message;
    }
}
